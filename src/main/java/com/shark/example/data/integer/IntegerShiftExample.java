package com.shark.example.data.integer;

public class IntegerShiftExample {
    public static void main(String[] args) {
        int value = 8 << 1;
        System.out.println("8 << 1: " + value);
        value = 8 << 4;
        System.out.println("8 << 4: " + value);
        value = 8 >> 1;
        System.out.println("8 >> 1: " + value);
    }
}
